#include <iostream> // เพิ่มไลบรารีสำหรับ input/output

int main() {
    int score; // ประกาศตัวแปรสำหรับเก็บคะแนน

    // แสดงข้อความต้อนรับและให้ผู้ใช้ป้อนคะแนน
    std::cout << "--------------------------------\n";
    std::cout << "     โปรแกรมคำนวณเกรด\n";
    std::cout << "--------------------------------\n";
    std::cout << "กรุณาป้อนคะแนนของคุณ (0-100): ";
    std::cin >> score; // รับค่าคะแนนจากผู้ใช้

    // ตรวจสอบความถูกต้องของคะแนน
    if (score < 0 || score > 100) {
        std::cout << "Invalid data, please try again...\n"; // แสดงข้อความแจ้งเตือนถ้าคะแนนไม่ถูกต้อง
    } else {
        // เงื่อนไขในการตัดเกรด
        if (score >= 80) {
            std::cout << "คุณได้เกรด: 4\n";
        } else if (score >= 75) {
            std::cout << "คุณได้เกรด: 3.5\n";
        } else if (score >= 70) {
            std::cout << "คุณได้เกรด: 3\n";
        } else if (score >= 65) {
            std::cout << "คุณได้เกรด: 2.5\n";
        } else if (score >= 60) {
            std::cout << "คุณได้เกรด: 2\n";
        } else if (score >= 55) {
            std::cout << "คุณได้เกรด: 1.5\n";
        } else if (score >= 50) {
            std::cout << "คุณได้เกรด: 1\n";
        } else {
            std::cout << "คุณได้เกรด: 0\n";
        }
    }

    return 0; // คืนค่า 0 เพื่อบอกว่าโปรแกรมทำงานเสร็จสมบูรณ์
}
